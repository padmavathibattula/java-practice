package com.java.example;

public class Sample {
	public static void main(String[] args) {
		// example of for loop
		System.out.println("********** for loop ************");
		for (int i = 0; i < 10; i++) {
			System.out.println(i + "padmavathi");
		}

		System.out.println("********** while loop ************");
		int j = 0;
		while (j < 10) {
			System.out.println(j + "padmavathi");
			j++;

		}

		System.out.println("********** do while loop ************");
		int k = 0;
		do {
			System.out.println(k + "padmavathi");
			k++;
		} while (k < 10);
	}

}
